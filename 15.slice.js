const animals = ["ant", "bison", "camel", "duck", "elephant"];

console.log(animals.slice(2)); // extract the array from index 2

console.log(animals.slice(2, 4)); // extract the array between indices 2 & 4

console.log(animals.slice(1, 5));

console.log(animals.slice(-2)); // extract the array from last two elements

console.log(animals.slice(2, -1)); // extract the array from index 2 to the index excluding last index of the array
